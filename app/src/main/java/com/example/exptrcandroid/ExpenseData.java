package com.example.exptrcandroid;

public class ExpenseData {

    public ExpenseData(String name, double price, String info, int day, int month, int year)
    {
        Name = name;
        Price = price;
        Info = info;
        Day = day;
        Month = month;
        Year = year;
    }

    public String Name;
    public double Price;
    public String Info;
    public int Day;
    public int Month;
    public int Year;

}
